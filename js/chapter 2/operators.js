/* Types of Operators in JavaScript ⚙️
1. Arithmetic Operators

Used for mathematical operations.

+   -   *   /   %   **   ++   --


Example:

let a = 10 + 5;

2. Assignment Operators

Used to assign values.

=   +=   -=   *=   /=   %=   **=


Example:

x += 5;

3. Comparison Operators

Used to compare values (returns boolean).

==   ===   !=   !==   >   <   >=   <=


Example:

a === b

4. Logical Operators

Used with conditions.

&&   ||   !


Example:

a > 5 && b < 10

5. Bitwise Operators

Operate on bits (low-level).

&   |   ^   ~   <<   >>   >>>

6. String Operators

Used with strings.

+   +=


Example:

"Hello " + "World"

7. Ternary (Conditional) Operator

Shortcut for if-else.

condition ? value1 : value2


Example:

age >= 18 ? "Adult" : "Minor"

8. Type Operators

Used to check or convert types.

typeof   instanceof

9. Unary Operators

Work on single operand.

++   --   +   -   !   typeof   delete

10. Relational Operators

Used to check relationships.

in   instanceof

11. Spread & Rest Operators

Modern JS.

... 


Example:

let arr2 = [...arr1];

12. Nullish & Optional (Modern)
??   ?. 


Example:

user?.name

13. Comma Operator
,


Example:

let x = (1, 2, 3); // x = 3
 */